function whichImageLoad() {
  var chica = 480;
  var mediana = 800; //768
  var root = (typeof exports === 'undefined' ? window : exports);
  var mediaQuery = '(-webkit-min-device-pixel-ratio: 2), (min--moz-device-pixel-ratio: 2), (-o-min-device-pixel-ratio: 2/1), (min-device-pixel-ratio: 2), (min-resolution: 192dpi), (min-resolution: 2dppx)';
  var isRetina = false;
  var result = "1";

  if (root.devicePixelRatio >= 2) {
    isRetina = true;
  }

  if (root.matchMedia && root.matchMedia(mediaQuery).matches) {
    isRetina = true;
  }

  var windWidtht = $(window).width();


  if (isRetina) {
    if (windWidtht > mediana) {
      // grande retina
      result = "1";
    } else if (windWidtht <= mediana && windWidtht > chica) {
      // mediano retina
      result = "2";
    } else {
      // chico retina
      result = "3";
    }

  } else {
    if (windWidtht > mediana) {
      // grande retina
      result = "4";
    } else if (windWidtht <= mediana && windWidtht > chica) {
      // mediano retina
      result = "5";
    } else {
      // chico retina
      result = "6";
    }
  }

  return result;
};

