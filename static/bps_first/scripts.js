// Plugin Accesible Drop Down
// https://uablogs.missouri.edu/interface/2011/08/keyboard-accessible/
$.fn.accessibleDropDown = function () {
  var el = $(this);

  var className = el.attr('class');

  // Dropdowns facetas
  if (className == 'faceta temas-destacados') {
    $("a", el).focus(function () {
      $(".temas-destacados").addClass("hover");
    }).blur(function () {
      $(".temas-destacados").removeClass("hover");
    });

  // Menu principal
  } else {
    $("li", el).mouseover(function () {
      $(this).addClass("hover");
      $(this).find('.submenu').toggleAttr('aria-hidden', 'true', 'false');
    }).mouseout(function () {
      $(this).removeClass("hover");
      $(this).find('.submenu').toggleAttr('aria-hidden', 'true', 'false');
    });

    $(".item > a").focus(function () {
      $(this).next('.submenu').toggleAttr('aria-hidden', 'true', 'false');
    }).blur(function () {
      $(this).next('.submenu').toggleAttr('aria-hidden', 'true', 'false');
    });

    $("a", el).focus(function () {
      $(this).parents("li").addClass("hover");
      $(this).closest('.submenu').toggleAttr('aria-hidden', 'true', 'false');
    }).blur(function () {
      $(this).parents("li").removeClass("hover");
      $(this).closest('.submenu').toggleAttr('aria-hidden', 'true', 'false');
    });
  }
};


// Solucionar problema de foco al saltar al contenido
// https://www.nczonline.net/blog/2013/01/15/fixing-skip-to-content-links/
( function() {
	var is_webkit = navigator.userAgent.toLowerCase().indexOf( 'webkit' ) > -1,
	    is_opera  = navigator.userAgent.toLowerCase().indexOf( 'opera' )  > -1,
	    is_ie     = navigator.userAgent.toLowerCase().indexOf( 'msie' )   > -1;

	if ( ( is_webkit || is_opera || is_ie ) && document.getElementById && window.addEventListener ) {
		window.addEventListener( 'hashchange', function() {
			var id = location.hash.substring( 1 ),
				element;

			if ( ! ( /^[A-z0-9_-]+$/.test( id ) ) ) {
				return;
			}

			element = document.getElementById( id );

			if ( element ) {
				if ( ! ( /^(?:a|select|input|button|textarea)$/i.test( element.tagName ) ) ) {
					element.tabIndex = -1;
				}

				element.focus();
			}
		}, false );
	}
})();


